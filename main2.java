public class main2 {
    public static void main(String[] args) {

//      variables

//        integers
//        declaration:
            int a ;

//        assignment:
            a = 123;
            System.out.println(a);

//        initialization:
            int b = 456;
            System.out.println(b);

            System.out.println("my number is " + b);

//        long data type for longer numbers
            long c = 1234567890L;
            System.out.println("my new number is " + c);

//        strings
            String name = "sly zoo";
            System.out.println("hello "+name);

//        boolean
        boolean d = true;
            System.out.println("are you enrolled in school?"+d);

//        float
            float e = 1.14f;
            System.out.println(e);

//        double
            double f = 3.14159;
            System.out.println(f);

//        char
            char symbol = '#';
            System.out.println(symbol);
    }
}
